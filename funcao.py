print("Meu programa que usa função")
# Definição da Função multiplica
def multiplica(num1, num2):
    multiplicacao = num1 * num2
    return multiplicacao

# Definição da Função soma
def soma(num1, num2):
    soma = num1 + num2
    return soma

# Chamada da função multiplica
resultado1 = multiplica(2, 5)
print("O resultado de 2 * 5 é:")
print(resultado1)

# Chamada da função soma
resultado2 = soma(2, 5)
print("O resultado de 2 + 5 é:", resultado2)
resultado3 = soma(10, 20)
print("O resultado de 10 + 20 é:", resultado3)
